%% function
function modelfunction=modelfunction(parameters,t)
global k1 n1 k2 n2 cA time

    k1=parameters(1);
    n1=parameters(2);
    k2=parameters(3);
    n2=parameters(4);

    tspan = linspace(0,time(end),length(cA));
    [t,y]=ode23s(@batchReactor,tspan,[cA(1),0,0]);

    modelfunction=y(:,1);

end

function batchReactor=batchReactor(t,c)
global k1 n1 k2 n2  

NS = 3; 
NR = 2;
nu = [-1,1,0
    -1,0,1];

k = [k1,k2];
n = [n1,n2];

rate = zeros(3,1);

for j = 1:NR
    for i = 1:NS
        rate(i) = rate(i) + nu(j,i)*k(j)*c(1)^n(j);
    end
end

for i = 1:NS
    batchReactor(i) = rate(i);
end

batchReactor=batchReactor';
end

